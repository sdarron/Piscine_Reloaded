int	ft_sqrt(int nb)
{
	int a;
	int pr;
	int rez;

	rez = 0;
	a = 0;
	pr = 0;
	while (pr <= nb)
	{
		pr = a * a;
		if (pr == nb)
			rez = a;
		a++;
	}
	return (rez);
}
