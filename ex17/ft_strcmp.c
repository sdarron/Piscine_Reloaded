int	ft_strcmp(char *s1, char *s2)
{
	int j;
	int i;
	int rez;

	j = 0;
	i = 0;
	rez = 0;
	while ((s1[i] != '\0') && (s2[j] != '\0') && (s1[i] == s2[j]))
	{
		i++;
		j++;
	}
	rez = s1[i] - s2[j];
	return (rez);
}
